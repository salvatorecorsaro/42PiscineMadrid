int	ft_atoi(char *str)
{
	int i;
	int sign;
	int r;

	i = 0;
	sign = 0;
	r = 0;
	while ((str[i] == ' ') || (str[i] == '\r') ||
			(str[i] == '\f') || (str[i] == '\v') ||
			(str[i] == '\n') || (str[i] == '\t'))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = (10 * r) + str[i] - '0';
		i++;
	}
	if (sign % 2 == 1)
		return (-r);
	return (r);
}
